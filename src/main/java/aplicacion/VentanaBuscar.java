    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        PreparedStatement ps = null;

        try {
            Conexion con = new Conexion();
            Connection conexion = con.getConnection();

            ps = conexion.prepareStatement("update producto set codigo=?,nombre=?,precio=?,cantidad=? where codigo=?");
            ps.setString(1, jtextCodigo.getText());
            ps.setString(2, jtextNombre.getText());
            ps.setDouble(3, Double.parseDouble(jtextPrecio.getText()));
            ps.setInt(4, Integer.parseInt(jtextCantidad.getText()));
            ps.setString(5, jtextCodigo.getText());

            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Registro modificado correctamente");

            ventanaPrincipal.consulta();

            panel.setVisible(false);
            setSize(650, 373);
            botonModificar.setEnabled(true);
            botonEliminar.setEnabled(true);

        } catch (Exception e) {
            System.out.println("Error: " + e);
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

