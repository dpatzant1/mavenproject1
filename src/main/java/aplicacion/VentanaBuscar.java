    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed

            ventanaPrincipal.consulta();
            valor = codigo.getText();

            DefaultTableModel modeloTabla = new DefaultTableModel();
            tabla.setModel(modeloTabla);

            String where = "";

            if (!"".equals(valor)) {
                try{
                    int codigoInt = Integer.parseInt(valor);
                    where = "where codigo = "+ codigoInt+ " or nombre like '%"+ valor +"%'";
                }catch(NumberFormatException e){
                    where = "where nombre like '%"+ valor +"%'";
                }
            }

            PreparedStatement ps = null;
            ResultSet rs = null;

            try {
                Conexion con = new Conexion();
                Connection conexion = con.getConnection();

                ps = conexion.prepareStatement("select codigo,nombre,precio,cantidad from producto " + where);
                rs = ps.executeQuery();

                if (rs.next()) {
                    modeloTabla.addColumn("Código de Producto");
                    modeloTabla.addColumn("Nombre de Producto");
                    modeloTabla.addColumn("Precio Unitario");
                    modeloTabla.addColumn("Cantidad Existente");

                    do {
                        Object fila[] = new Object[4];
                        for (int i = 0; i < 4; i++) {
                            fila[i] = rs.getObject(i + 1);
                        }
                        jtextCodigo.setText(rs.getString("codigo"));
                        jtextNombre.setText(rs.getString("nombre"));
                        jtextPrecio.setText(String.valueOf(rs.getDouble("precio")));
                        jtextCantidad.setText(String.valueOf(rs.getInt("cantidad")));
                        modeloTabla.addRow(fila);
                    } while (rs.next());
                    botonModificar.setEnabled(true);
                    botonEliminar.setEnabled(true);

                } else {
                    modeloTabla.addColumn("Código de Producto");
                    modeloTabla.addColumn("Nombre de Producto");
                    modeloTabla.addColumn("Precio Unitario");
                    modeloTabla.addColumn("Cantidad Existente");
                    JOptionPane.showMessageDialog(null, "Producto no encontrado");
                    botonModificar.setEnabled(false);
                    botonEliminar.setEnabled(false);
                }

            } catch (Exception e) {
                System.out.println("Error: " + e);
            }
            panel.setVisible(false);
            setSize(650, 373);
    }//GEN-LAST:event_botonBuscarActionPerformed

